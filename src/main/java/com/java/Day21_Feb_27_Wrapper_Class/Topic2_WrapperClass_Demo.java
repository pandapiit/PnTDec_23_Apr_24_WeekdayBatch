package com.java.Day21_Feb_27_Wrapper_Class;

public class Topic2_WrapperClass_Demo {
	
	//int - Integer
	//long - Long
	//short - Short
	//boolean - Boolean
	//double - Double
	//char - Character
	//float - Float
	//byte - Byte
	
	//Wrapper classes are pre-defined classes which can contain primitive data type.
	//They wrap primitive data type to Wrapper Class Object
	
	public static void main(String[] args) {
		int a = 10;
		System.out.println("The primitive data type value is : " + a);
		
		
		Integer b = 10;
		System.out.println("The Wrapper class object value is : " + b);
		
		
		
	}

}

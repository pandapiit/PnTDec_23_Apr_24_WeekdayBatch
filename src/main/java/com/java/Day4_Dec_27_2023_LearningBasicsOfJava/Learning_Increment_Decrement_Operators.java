package com.java.Day4_Dec_27_2023_LearningBasicsOfJava;

public class Learning_Increment_Decrement_Operators {

	public static void main(String[] args) {
	
		
		//Arithmetic Operators   ++ (increment)       -- (decrement)
		
		//     ++ represents +1     and    -- represents -1
		
		
	    int i=1;
	//  datatype      variable 
		//in the memory location the variable i has a value stored as 1
		
		i = 2;   //we have updated the value of variable 1. Earlier it was 1 and now it has been updated to 2
		i = 3;
		System.out.println("The latest value of i is : " + i);
		i = 4;
		System.out.println("The latest value of i is : " + i);
		
		i = 5; //this is the latest value of i
		
		System.out.println("The latest value of i is : " + i);
		
		

	}

}

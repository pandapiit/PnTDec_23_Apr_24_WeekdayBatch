package com.java.Day4_Dec_27_2023_LearningBasicsOfJava;

public class Inc_Dec_Operators_Example1 {

	public static void main(String[] args) {
		// ++ represents +1 and -- represents -1
		//post increment, pre increment
		//post decrement, pre decrement
		
		int i=1;  //latest value of i is 1
		i++;   //this is an example of post increment operator //latest value of i will become 2
		++i;   //this is an example of pre increment operator  //latest value of i is 3
		i-- ;   //example of post decrement //latest value of is will become 2
		--i;    //example of pre decrement //latest value is 1
		
		//let's answer this question
		
		System.out.println("The value of i is : " + i);
		
		
		
		

	}

}
